#concept of relational operators
a = int(input("Enter value for a"))
b = int(input("Enter value for b"))

result = a == b #True or False
print(f"{result} = {a} == {b}") 

result = a != b 
print(f"{result} = {a} != {b}") 

result = a < b 
print(f"{result} = {a} < {b}") 

result = a > b 
print(f"{result} = {a} > {b}") 

result = a <= b 
print(f"{result} = {a} <= {b}") 

result = a >= b 
print(f"{result} = {a} >= {b}") 
